let myLetVar: string = "ciao";
myLetVar = "ciao2";

const myConstant: string = "ciao";

// TYPE NUMBER 
let myNumber: number = 8;

// TYPE BOOLEAN
let myBoolean: boolean = true;

// TYPE UNDEFINED
let myUndefinedVar: undefined;

// TYPE NULL
let myNullVAr: null;

// TYPE OBJECT
let myObject: object = {};

// TYPE ANY (qualsiasi)
let myAnyVar: any;
myAnyVar = "ciao";
myAnyVar = 10;
myAnyVar = false;

